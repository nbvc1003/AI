


nums = []

for i in range(5):
    nums.append(i)


print('nums =',nums)

print('nums[2:4] = ', nums[2:4])
print('nums[2:] = ', nums[2:])
print('nums[:2] = ', nums[:2])
print('nums[:] = ', nums[:])
print('nums[:-1] = ', nums[:-1]) # 마지막 만 빼고 
print('nums[-1:] = ', nums[-1:]) # 마지막 하나만

nums[2:4] = [8,9] # 중간에 값을 끼워넣기

print('nums = ', nums)







